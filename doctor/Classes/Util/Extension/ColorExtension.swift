import UIKit

extension UIColor {
    
    // MARK: - 文字颜色
    /// 用于标题、重要文本
    static var c3: UIColor {
        return UIColor(stringHexValue: "#333333")!
    }
    
    /// 用于次级文本
    static var c6: UIColor {
        return UIColor(stringHexValue: "#666666")!
    }
    
    /// 用于次级文本及icon色
    static var c9: UIColor {
        return UIColor(stringHexValue: "#999999")!
    }
    
    
    static var cf5f5f5: UIColor {
        return UIColor(stringHexValue: "#f5f5f5")!
    }
    
    static var c4167f3: UIColor {
        return UIColor(stringHexValue: "#4167F3")!
    }
    
    static var cfbfbfb: UIColor {
        return UIColor(stringHexValue: "#FBFBFB")!
    }
}

